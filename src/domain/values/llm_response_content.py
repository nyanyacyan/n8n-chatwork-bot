# import
from dataclasses import dataclass


# ----------------------------------------------------------------------------------
# **********************************************************************************


@dataclass(frozen=True)
class LLMResponseContent:
    value: str

    def __post_init__(self):
        if not self.value:
            raise ValueError("Responseが空です")

    def length(self) -> int:
        return len(self.value)

