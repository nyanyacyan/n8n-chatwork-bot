# import
from dataclasses import dataclass


# ----------------------------------------------------------------------------------
# **********************************************************************************

@dataclass(frozen=True)
class PromptContent:
    value: str

    # 不正な値を除外
    def __post_init__(self):
        if not self.value:
            raise ValueError("プロンプトが空です")

        if len(self.value) > 2000:
            raise ValueError("Promptは2000文字以内にしてください")

