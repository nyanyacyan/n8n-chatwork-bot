# import
from dataclasses import dataclass


# ----------------------------------------------------------------------------------
# **********************************************************************************

@dataclass(frozen=True)
class ReplyContent:
    value: str

    # 不正な値を除外
    def __post_init__(self):
        if not self.value:
            raise ValueError("送信メッセージの値がありません")

